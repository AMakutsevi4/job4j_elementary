package ru.job4j.condition;

public class Liken {

    public static void main(String[] args) {
        int first = 10;
        int second = 9;

        boolean resultMore = first > second;
        System.out.println(resultMore);

        boolean resultLess = first < second;
        System.out.println(resultLess);

        boolean resultEquals = first == second;
        System.out.println(resultEquals);
    }
}
